package chapter6;

public class Printer {
	// 매서드
	void println(int x) {
		System.out.println(x);
	}

	void println(boolean x) {
		System.out.println(x);

	}

	void println(double x) {
		System.out.println(x);

	}

	void println(String x) {
		System.out.println(x);
		
	}
	static void println(char x) { //정적 메소드 
		System.out.println(x);
	}

}
